# 향상된 유저 프롬프트 빌더
# ─────────────────────────────────────────────────────────────

def build_enhanced_user_prompt(
    section_name: str,
    evaluation_item: str,
    max_score: int,
    page_target: int,
    rfp_context: str,
    company_context: str = "",
    profile_md: str = "",
    strategy_memo_text: str = "",
    instructions: str = "",
) -> str:
    """Layer 1 검색 대신 구조화된 프롬프트로 섹션 작성 지시."""
    parts: list[str] = []

    # 1. 회사 역량 (Layer 2 — RAG 유지)
    if company_context:
        parts.append(f"## 당사의 역량 및 실적:\n{company_context}")

    # 2. 회사 프로필/스타일 (Layer 2)
    if profile_md:
        parts.append(f"## 당사의 제안서 프로필 (문체와 구조를 준수):\n{profile_md}")

    # 3. 전략 메모
    if strategy_memo_text:
        parts.append(f"## 이 섹션의 전략 (반드시 반영):\n{strategy_memo_text}")

    # 4. 공고 정보 (핵심)
    parts.append(f"## 이번 공고 정보:\n{rfp_context}")

    # 5. 작성 지시
    char_target = page_target * 500
    task = (
        f"## 작성할 섹션\n"
        f"- **섹션명**: {section_name}\n"
        f"- **평가항목**: {evaluation_item}\n"
        f"- **배점**: {max_score}점\n"
        f"- **목표 분량**: 약 {page_target}페이지 ({char_target}자 이상)\n"
        f"\n"
        f"7대 원칙을 모두 적용하여 이 섹션을 작성하세요.\n"
        f"평가항목 '{evaluation_item}'의 키워드가 본문에 자연스럽게 3회 이상 등장해야 합니다."
    )
    if instructions:
        task += f"\n\n추가 지침: {instructions}"

    parts.append(task)

    return "\n\n".join(parts)
