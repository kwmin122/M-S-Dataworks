def get_vague_patterns(domain: str = "") -> list[str]:
    """도메인에 맞는 금지 패턴 목록 반환.

    Args:
        domain: 'it_sw', 'construction', 'consulting', 'goods', 또는 빈 문자열(범용)
    """
    patterns = list(UNIVERSAL_VAGUE_PATTERNS)
    if domain in DOMAIN_VAGUE_PATTERNS:
        patterns.extend(DOMAIN_VAGUE_PATTERNS[domain])
    return patterns


